    //----------------------------------------------------------------------------//
    // 1. Método donde la computadora selecciona de forma aleatoria una opción   //
    //     [Piedra = 0] ~ [Papel = 1] ~ [Tijera = 2]                              //
    //----------------------------------------------------------------------------//
    public void cpuEleccion(){
        CPU_manoEleccion = random.nextInt(3);
        
        switch(CPU_manoEleccion){
            case 0:
                CPU_Roll.setIcon(new ImageIcon(getClass().getResource("Imagenes/CPU_Piedra.png")));
                break;
            case 1:
                CPU_Roll.setIcon(new ImageIcon(getClass().getResource("Imagenes/CPU_Papel.png")));
                break;
            case 2:
                CPU_Roll.setIcon(new ImageIcon(getClass().getResource("Imagenes/CPU_Tijeras.png")));
                break;
        }
    }
    
    //----------------------------------------------------------------------------//
    // 2. Método que reinicia los botones para volver a jugar.                   //
    //----------------------------------------------------------------------------//
    public void reiniciarBotones(){
        
        Boton_Piedra.setVisible(false);
        Boton_Papel.setVisible(false);
        Boton_Tijera.setVisible(false);
        
        Boton_Jugar.setVisible(true);
    }

    //----------------------------------------------------------------------------//
    // 3. Método que realiza el conteo de las partidas ganadas por el jugador.    //
    //----------------------------------------------------------------------------// 
    public void puntajeContadorAum(){
        contadorPuntaje++;
        Jugador_Puntaje.setText(String.valueOf(contadorPuntaje));       
    }
    
    //----------------------------------------------------------------------------//
    // 3. Método que realiza el conteo de las partidas perdidas por el jugador.    //
    //----------------------------------------------------------------------------// 
    public void puntajeContadorDism(){
        contadorPuntaje--;
        Jugador_Puntaje.setText(String.valueOf(contadorPuntaje));       
    }
    
    //----------------------------------------------------------------------------//
    // 4. Método que muestra la animación(.gif) de explosion de fondo.           //
    //     Cuando el contador de tiempo es igual a 2 seg la imagen es cambiada.   //
    //----------------------------------------------------------------------------//  
    public void explosionAnimacion(){
        temporizador = new Timer();
        
        Explosion.setIcon(new ImageIcon(getClass().getResource("Imagenes/Explosion(1).gif")));
        Explosion.setVisible(true);
        
        usoTemporizador = new TimerTask() {
            @Override
            public void run() {
                if(contadorTiempo == 2){
                    cancel();
                    Explosion.setIcon(new ImageIcon(getClass().getResource("Imagenes/Explosion(2).gif")));
                }
                contadorTiempo++;
            }
        };
        temporizador.schedule(usoTemporizador, 0, 1000);
        contadorTiempo = 0;
    }
    
