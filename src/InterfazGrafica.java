        JugadorRoll.setVisible(true);
        cpuEleccion();
        explosionAnimacion();
        
        switch(CPU_manoEleccion){
            case 0:
                Estado_Tag.setIcon(new ImageIcon(getClass().getResource("Imagenes/Empate_Tag.png")));
                reiniciarBotones();
                break;
            case 1:
                Estado_Tag.setIcon(new ImageIcon(getClass().getResource("Imagenes/Perdiste_Tag.png")));
                puntajeContadorDism();
                reiniciarBotones();
                break;
            case 2:
                Estado_Tag.setIcon(new ImageIcon(getClass().getResource("Imagenes/Ganaste_Tag.png")));
                puntajeContadorAum();
                reiniciarBotones();
                break;
        }
